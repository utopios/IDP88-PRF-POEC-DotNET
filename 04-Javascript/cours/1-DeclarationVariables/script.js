// Déclarer une variable (en ES5)
var prenom;
var age;

// Affichage du type
console.log(typeof(prenom));
console.log(typeof(age));

// Affectation d'une valeur à la variable
prenom = "Anthony";
age = 25;

// Afficher la valeur de cette variable dans la console du navigateur
console.log(prenom);
console.log(age);

// Affichage du type
console.log(typeof(prenom));
console.log(typeof(age));

// Concaténation du résultat dans une chaine
console.log("Bonjour, je m'appel "+prenom+" et j'ai "+age+" ans.");
console.log(`Bonjour, je m'appel ${prenom} et j'ai ${age} ans.`);

// Type Grand nombre (BigInt)
var grandNombre = 9007199254740992n;
console.log(typeof(grandNombre));

// Type null (null)
var typeNull = null;
console.log(typeof(typeNull));

// Type Booléenne (bool)
var typeBool = true;
